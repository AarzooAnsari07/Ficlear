// Helper function to filter PIN codes by partial match
export function searchPopularPinCodes(query: string): typeof popularPinCodes {
  if (!query || query.length < 3) return [];
  
  return popularPinCodes.filter(pc => 
    pc.pincode.startsWith(query) || 
    pc.area.toLowerCase().includes(query.toLowerCase())
  ).slice(0, 10); // Return top 10 matches
}
